
// using for loop
function factorial(n){
    let fact = 1;
    for (let i = 1; i <= n; i++) {
   
    fact = fact * i;

    console.log(i, fact);
    
}
}
factorial(5);


// while loop using
function f(n){
    var i = 1;
var facto = 1;
while(i <= n){
     facto = facto * i;
     i++;
}
console.log(i ,facto);

}

f(5);



